    public ScegliScheda(String email, String parte, String giorni, String giorniOre, String mattina,String pomeriggio,String sera, String kcal, double fabbisogno ) {
        initComponents();
        ImageIcon icon = new ImageIcon("src/main/resources/imageicon.png");
        setIconImage(icon.getImage());
        setTitle("GYM FOR YOU");
        this.email = email;
        this.parte = parte;
        this.giorni = giorni;
        this.giorniOre = giorniOre;
        this.mattina = mattina;
        this.pomeriggio = pomeriggio;
        this.sera = sera;
        this.kcal = kcal;
        this.fabbisogno = fabbisogno;
        
        combobox.setVisible(false);
        jLabel2.setVisible(false);
        comboboxtrainer.setVisible(false);
                
        
        schedaCorso = new SchedaCorso();
        Map dati = schedaCorso.prelevaCorsi();
        for (int i = 1; i < dati.size(); i++) {
            combobox.addItem((String)dati.get(""+i));
        }
        
        schedaPersonalizzata = new SchedaPersonalizzata();
        Map trainer = schedaPersonalizzata.prelevaTrainer();
        for (int i = 1; i < (trainer.size()/3)+1; i++) {
            String info = (String)trainer.get("email_"+i) + "/ "+ (String)trainer.get("nome_"+i) + "/ " + (String)trainer.get("cognome_"+i);
            comboboxtrainer.addItem(info );
        }
    }

