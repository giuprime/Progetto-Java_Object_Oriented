    private void inviabottoneMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inviabottoneMouseClicked
        // TODO add your handling code here:
        String tipo;
        
        if (jRadioButton1.isSelected()) {
            schedaPersonalizzata = new SchedaPersonalizzata();
            tipo = "Scheda Personalizzata";
            String emailtrainer = (String) comboboxtrainer.getSelectedItem();
            String newString = emailtrainer.substring(0, emailtrainer.indexOf("/"));
            schedaPersonalizzata.inputUtenteTrainer(email, tipo, parte, giorniOre, mattina, pomeriggio, sera, kcal, newString, fabbisogno);

            Pannello_Utente pannello = new Pannello_Utente();
            pannello.setVisible(true);
            dispose();
        }else if(jRadioButton2.isSelected()){
            
            tipo = "" + combobox.getSelectedItem();
            schedaCorso = new SchedaCorso();
            String newString = schedaCorso.ottieniEmail(tipo);
            
            if (parte.contains("C") && tipo.equals("Boxe") || parte.contains("O") && tipo.equals("Boxe") || parte.contains("CO")&& tipo.equals("Boxe")) {
                int dialogResult = JOptionPane.showConfirmDialog(null, "Il corso che hai scelto non si addice alle parti del corpo che vuoi allenare\n Desideri andare avanti lo stesso?", "ATTENZIONE!", JOptionPane.YES_NO_OPTION);
                if(dialogResult == JOptionPane.YES_OPTION){
                    schedaCorso.inputUtenteTrainer(email, tipo, parte, giorniOre, mattina, pomeriggio, sera, kcal, newString, fabbisogno);
                    Pannello_Utente pannello = new Pannello_Utente();
                    pannello.setVisible(true);
                    dispose();
                }
                
            }else{
                schedaCorso.inputUtenteTrainer(email, tipo, parte, giorniOre, mattina, pomeriggio, sera, kcal, newString, fabbisogno);
                Pannello_Utente pannello = new Pannello_Utente();
                pannello.setVisible(true);
                dispose();
            }
            
            
            
            
        }       
        
        
    }//GEN-LAST:event_inviabottoneMouseClicked

